		System.out.println(long1);
		
		double double1 = 279.15d;
		System.out.println(double1);
		
		boolean boolean1 = false;
		System.out.println(boolean1);
		
		Float float1 = 0.12f;
		System.out.println(float1);
		
		Integer integer1 = 8;
		System.out.println(integer1);
		
